#wap in python to show Collection Data Structure 

lst = [10,20,30]
print(type(lst))
print(len(lst))

#empty list of dummy list 
elst = []
print(type(elst))
print(len(elst))

#singleton : element = 1
slst = [10]
print(type(slst))
print(len(slst))

#tuple 
t = (10,20,30)
print(type(t))
print(len(t))

#empty tuple 
e_t = ()
print(type(e_t))
print(len(e_t))

e_t = tuple()
print(type(e_t))
print(len(e_t))

#singleton tuple 
s_t = (10,)
print(type(s_t))
print(len(s_t))

#dictionary
d = {'name':'ravi','age':20}
print(type(d))
print(len(d))

e_d = {}
print(type(e_d))
print(len(e_d))

s_d = {'key':10}
print(type(s_d))
print(len(s_d))

#set :- 
st = {10,20,20,10,30}
print(st)
print(type(st))
print(len(st))

#empty set
e_st = set({})
print(e_st)
print(type(e_st))
print(len(e_st))

#singleton set
s_st = set({10})
print(s_st)
print(type(s_st))
print(len(s_st))

#singleton Set using dict 
ss_st = {20}
print(ss_st)
print(type(ss_st))
print(len(ss_st))






































